# Complete the countApplesAndOranges function below.
def countApplesAndOranges(s, t, a, b, apples, oranges):
    ap_num, or_num = 0, 0
    for d in apples:
        if s <= (a + d) <= t:
            ap_num += 1
    for d in oranges:
        if s <= (b + d) <= t:
            or_num += 1
    print(ap_num, or_num, sep="\n")
    

